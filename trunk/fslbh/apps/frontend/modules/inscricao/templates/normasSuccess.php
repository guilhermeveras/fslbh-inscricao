<p>
<strong>Processo de seleção</strong><br />
O presente edital apresenta informações e regras gerais para o processo de inscrição no Curso de 
Especialização em Gestão Hospitalar - ProHosp V.
</p>


<br />


<p>
<strong>1) Inscrições on-line: 27/07/2009 a 07/08/2009</strong> <br />
A única forma de inscrição será online.<br />

<br />

No ato da inscrição o candidato deverá preencher a ficha de cadastro e escolher um login e uma senha para 
anexar o <strong>curriculum vitae</strong> e <strong>memorial</strong> na opção <strong>Submeter Anexos</strong>;<br />

<br />

Em agosto de 2009 será definido o período das inscrições no Curso de Especialização em Gestão Hospitalar - 
Turmas descentralizadas a ser realizado em  Alfenas, Juiz de Fora e Montes Claros.<br />

<br />

As informações relativas ao Curso serão divulgadas no site da Escola de Saúde Pública do Estado de Minas Gerais 
a partir  do dia 17/08/2009.
</p>

<br />



<p>
<strong>2) Submeter documentos</strong> <br />
Após fazer a <strong>Inscrição</strong> no sistema, o candidato deverá submeter os seguintes documentos:
</p>

<br />

<div style="padding-left:30px;">

<p>
<strong>2.1) Curriculum Vitae</strong><br />
</p>

<br />

<p>
<strong>2.2) Memorial</strong><br />
O memorial é constituído por narrativa em que o canditato apresenta uma reflexão sobre suas experiências de trabalho, 
com base em sua trajetória acadêmico-profissional e, a partir desta reflexão, indica as suas motivações e razões para 
buscar a Pós-graduação em Gestão Hospitalar.
<br /><br />
O memorial deve conter no máximo 01 (uma) lauda digitada com fonte Arial, corpo 12, espaço simples, sem linha entre os 
parágrafos, todas as margens com 02 (dois) centímetros. 
</p>

</div>

